#include "ft_printf.h"

int	ft_putstr(char const *s)
{
	int	i;

	if (s == NULL)
	{
		write (1, "(null)", 6);
		i = 6;
		return (i);
	}
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
